<script>
import authUnils from "../../../common/authUnils"
import qs from 'queryString'
export default{
    data(){
        return{
            isShowOrder:true,
            isShowExtendList:false,
            tableData:[],
            value:"",
            filters:{
                welfareType:"",
                startTime:"",
                endTime:"",
                pageNum:1,
                pageSize:10
            },
            totalSize:0,
            currentPage:1,
            orderDetailVisible:false,
            orderInfo:{},
            extendEmpTable:[],
            extendCurrentPage:1,
            extendtTotalSize:0,
            //发放人员参数
            extendEmpParams:{
                orderId:"",
                pageNum:1,
                pageSize:10,
                state:""
            }
        }
    },
    methods:{
        //格式化时间
        formatDate(time){
            var d=new Date(time)
            return d.getFullYear() + '-' + (d.getMonth() + 1) + '-' + d.getDate()
        },
        handleDate(){
            if(this.value==null){
                return 
            }
            this.filters.startTime=this.formatDate(this.value[0])
            this.filters.endTime=this.formatDate(this.value[1])
        },
        handleSizeChange(val){
            this.filters.pageSize=val
        },
        handleCurrentChange(val){
            this.filters.pageNum=val
        },
        //导出福利卷订单
        exportWelRollOrder(){
            this.$axios.get("/api/api/voucher/exportExcelVoucherOrder").then(res=>{
                console.log(res)
            })
        },
        //显示订单列表
        getPagedOrder(){
            this.$axios.post("/api/api/voucher/showOrder",this.filters,{
                headers:{
                    "Authorization":authUnils.getToken()
                }
            }).then(res=>{
                if(res.status==200){
                    if(res.data.code==0){
                        this.tableData=res.data.data.content
                        this.totalSize=res.data.data.totalSize
                    }else{
                        this.$alert(res.data.message,"信息")
                    }
                }
            })
        },
        //搜索结果
        getSearchResult(){
            this.getPagedOrder()
        },
        //查看详情
        seeOrderDetail(id){
            this.orderDetailVisible=true
            this.$axios.post("/api/api/voucher/orderDetail",{
                orderId:id,
                pageNum:1,
                pageSize:10,
                state:""
            }).then(res=>{
                if(res.data.code==0){
                    this.orderInfo=res.data.data
                }
            })
        },
        extendEmpList(id){
            this.orderDetailVisible=false
            this.isShowExtendList=true
            this.isShowOrder=false
            this.extendEmpParams.orderId=id
            this.$axios.post("/api/api/voucher/orderDetailEmp",this.extendEmpParams).then(res=>{
                console.log(res)
            })
        },
        //返回列表
        returnList(){
            this.isShowExtendList=false
            this.isShowOrder=true
        },
        handleExtendSize(val){
            this.extendEmpParams.pageSize=val
        },
        handleExtendCurrent(val){
            this.extendEmpParams.pageSize=val
        }
    },
    mounted(){
        this.getPagedOrder()
    }
}
</script>
